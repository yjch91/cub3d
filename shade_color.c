#include "cub3d.h"

int	shade_color(double dist, int c)
{
	int		result;
	double	n;

	n = dist / 1.5;
	if (n <= 1.0)
		n = 1.0;
	if (n >= 20.0)
		n = 20.0;
	result = ((int)(((0xFF0000 & c) >> 16) / n) << 16) +
		((int)(((0x00FF00 & c) >> 8) / n) << 8) +
		((int)((0x0000FF & c) / n));
	return (result);
}
